<h2>ChinaSys 2020</h2>
<p>
<p>ChinaSys workshop is a leading international forum focusing on premier research results for computer systems. All topic areas related to the design and implementation of computer systems are of interest and in scope, including but not limited to: operating systems, computer architecture, virtualization, distributed systems, computer networks, system security, cloud computing, edge computing&nbsp;and programming languages.&nbsp;The 19th ChinaSys workshop will be held in Chongqing&nbsp;from&nbsp;Dec. 5 to 6, 2020.</p>
<p>The aim of the workshop is to bring together researchers&nbsp;and practitioners from industry and academia on computer systems. The workshop contains invited talks from renowned researchers, “best of the rest” talks covering top research papers from recent top systems related conferences, as well as peer-reviewed talks describing most-recent progresses in our community.&nbsp;</p>
<p>Since August 2015, ChinaSys has become an ACM SIGOPS chapter, with a formal name called ACM SIGOPS ChinaSys.</p>
